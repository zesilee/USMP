#!/bin/bash
# 测试后代码检视流程 - Hook 脚本
# 详细说明见 .claude/HOOKS.md

cat << 'EOF'
【测试完成后触发代码检视】

检测到 go test 已执行完成，现在触发代码检视流程：

1. 执行 git diff HEAD --name-only 列出所有变更的 .go 文件
2. 使用 go-code-review-check 技能对变更文件进行检视
3. 输出检视报告，包含：
   - 变更文件列表
   - 发现的问题（严重程度）
   - 修改建议
   - 检视结论

4. 如果存在严重问题：
   - 标记【需要修复】
   - 列出必须修复的问题
   - 提醒用户：代码存在严重问题，请修复后再提交！

5. 如果检视通过：
   - 标记【检视通过，可安全提交】
   - 提醒用户可以提交代码

输出：完整的检视报告（中文）
EOF
